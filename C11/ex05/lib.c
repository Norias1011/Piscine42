#include <unistd.h>
#include "ft.h"

void	ft_putnbr(int nb)
{
	long	d;
	int		c;

	d = nb;
	if (d < 0)
	{
		write(1, "-", 1);
		d = d * -1;
	}
	if (d >= 10)
	{
		ft_putnbr(d / 10);
		d = d % 10;
	}
	if (d < 10)
	{
		c = d + '0';
		write(1, &c, 1);
	}
}
